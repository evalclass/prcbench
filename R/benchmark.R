#' Run microbenchmark with specified sets of tools and sets of sample datasets
#'
#' The \code{benchmark_sets} function runs
#'     \code{\link[microbenchmark]{microbenchmark}} for specified sets of tools
#'     and sets of sample datasets
#'
#' @param sampset_names A character vector to specify the names of sample sets
#'     for \code{\link{create_sampleset}}.
#'
#' @param toolset_names A character vector to specify the names of tool sets for
#'     \code{\link{create_tools}}.
#'
#' @param times The number of iteration used in
#'     \code{\link[microbenchmark]{microbenchmark}}.
#'
#' @param unit A single string to specify the unit used in
#'     \code{\link[microbenchmark]{summary.microbenchmark}}.
#'
#' @return A data frame with microbenchmark results.
#'
#' @seealso \code{\link{run_benchmark}} is used for benchmarking.
#'    \code{\link[microbenchmark]{microbenchmark}} for benchmarking details.
#'    \code{\link{create_sampleset}} to generate sample datasets.
#'    \code{\link{create_tools}} to create sets of tools.
#'
#'
#' @examples
#' ## Generate a sample dataset
#' res1 <- benchmark_sets(c("b100", "ib100"), c("crv", "auc", "both"))
#'
#' @export
benchmark_sets <- function(sampset_names = c("b100", "ib100", "b10k", "ib10k"),
                           toolset_names = c("crv", "auc", "both"),
                           times = 5, unit = "ms") {

  samps <- lapply(sampset_names, create_sampleset)
  names(samps) <- sampset_names
  tools <- lapply(toolset_names, create_tools)
  names(tools) <- toolset_names

  new_samp_names <- rep(sampset_names, length(toolset_names))
  new_tool_names <- rep(toolset_names, each = length(sampset_names))

  bmfunc <- function(i) {
    bres <- run_benchmark(samps[[new_samp_names[i]]],
                          tools[[new_tool_names[i]]],
                          times = times, unit = unit)
    bres$sampset <- new_samp_names[i]
    bres$toolset <- new_tool_names[i]
    bres
  }

  res <- lapply(seq_along(new_samp_names), bmfunc)
  df_res <- do.call(rbind, res)
  df_res
}

#' Run microbenchmark with a specified set of tools and a sample dataset
#'
#' The \code{run_benchmark} function runs
#'     \code{\link[microbenchmark]{microbenchmark}} for a specified set of tools
#'     and a sample dataset.
#'
#' @param sdat A sample dataset genereated by \code{\link{create_sampleset}} or
#'     \code{\link{create_rnd_sample}}.
#'
#' @param tool_funcs A list of functions generated by
#'     \code{\link{create_tools}}.
#'
#' @param times The number of iteration used in
#'     \code{\link[microbenchmark]{microbenchmark}}.
#'
#' @param unit A single string to specify the unit used in
#'     \code{\link[microbenchmark]{summary.microbenchmark}}.
#'
#' @return A data frame with microbenchmark results.
#'
#' @seealso \code{\link{create_sampleset}} and \code{\link{create_rnd_sample}}
#'    to generate a sample dataset. \code{\link{create_tools}} to create a
#'    set of tools. \code{\link[microbenchmark]{microbenchmark}} for
#'    benchmarking details.
#'
#' @examples
#' ## Generate a sample dataset
#' samp1 <- create_rnd_sample()
#' toolset1 <- create_tools("crv")
#' res1 <- run_benchmark(samp1, toolset1)
#'
#' @export
run_benchmark <- function(sdat, tool_funcs, times = 5, unit = "ms") {
  if (!requireNamespace("microbenchmark", quietly = TRUE)) {
    stop("microbenchmark needed for this function to work. Please install it.",
         call. = FALSE)
  }

  bmfunc <- function(tfunc) {
    mres <- microbenchmark::microbenchmark(tfunc(sdat), times = times)
    summres <- summary(mres, unit = unit)
    summres$expr <- names(tfunc)
    summres
  }

  res <- lapply(tool_funcs, bmfunc)
  df_res <- do.call(rbind, res)
  df_res$tool <- rownames(df_res)
  rownames(df_res) <- NULL
  df_res
}
